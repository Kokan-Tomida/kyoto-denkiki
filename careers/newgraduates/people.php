購買部では、直接、お客様とのやりとりや製造をするわけではありません。だからこそ、電源についてよく勉強し、理解することはとても大切だと考えています。入社した頃、文系大学出身だった私はわからないことだらけ。先輩に何がわからないのか聞かれても、何がわからないかすら答えられない状態でした。そこから、基礎知識を勉強し、同じ部署の先輩や上司はもちろん、製造現場で働く他部署の方や設計士の方にもわからないことは全て質問し、理解するよう心がけてきました。自分から学ぶ姿勢があれば、丁寧に教えてくれる、そんな環境があるので、億劫になることはなく、着実に吸収できていると思います。</p>
                <p class="people-contents-box02-p02">京都電機器を選んだ理由</p>
                <p class="people-contents-box02-p03">胸を張って製品を勧められる。
私が就職活動をする上で大切に考えていたことは、「自分たちがつくっているものに自信と誇りを持っている会社に入りたい」ということでした。自分自身、お客様に自信を持って勧められないものは売りたくない、と強く思っていました。説明会に参加したときに、先輩社員が「自社製品に自信を持って提案できる、それが誇り」と話していたことに感銘を受け、「自分もその力になりたい！」その一心で京都電機器への入社を決めました。
その想いは働いている今でも変わらず、自信を持って京都電機器の製品を勧めることができます、それが私の誇りです。</p>
